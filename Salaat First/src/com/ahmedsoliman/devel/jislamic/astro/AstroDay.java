package com.ahmedsoliman.devel.jislamic.astro;

public class AstroDay {
	private double dec;

	private double ra;

	private double sidtime;

	private double dra;

	private double rsum;

	public double getDec() {
		return dec;
	}

	public double getDra() {
		return dra;
	}

	public double getRa() {
		return ra;
	}

	public double getRsum() {
		return rsum;
	}

	public double getSidtime() {
		return sidtime;
	}

	public void setDec(double dec) {
		this.dec = dec;
	}

	public void setDra(double dra) {
		this.dra = dra;
	}

	public void setRa(double ra) {
		this.ra = ra;
	}

	public void setRsum(double rsum) {
		this.rsum = rsum;
	}

	public void setSidtime(double sidtime) {
		this.sidtime = sidtime;
	}

}
